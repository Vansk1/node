console.log("operadores aritméticos")
console.log("+= | soma e atribuição a variaável")
console.log("-= | subtração e atribuição a variaável ")
console.log("*= | multiplicação e atribuição a variaável")
console.log("/= | divisão e atribuição a variaável")
console.log("exemplos:")
let x = 10
console.log("x = 10")
console.log("10 += 5 = " + (x += 5))
console.log("15 -= 5 = " + (x -= 5))
console.log("10 *= 5 = " + (x *= 5))
console.log("50 /= 5 = " + (x /= 5))


